package dia5.sobrecargametodo;

public class Calculadora {

    public int sumar(int a, int b){//metodo con retorno suma dos enteros
        return a + b;
    }

    public double sumar(double a, double b){//metodo con retorno suma dos enteros
        return a + b;
    }

    public int sumar(int a, int b, int c){//metodo con 3 valores
        return a+b+c;
    }
}


